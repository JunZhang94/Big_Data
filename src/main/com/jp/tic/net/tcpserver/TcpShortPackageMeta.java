package com.jp.tic.net.tcpserver;

import java.util.Date;

public class TcpShortPackageMeta {
	private boolean isInited;
	private Object protocolMeta;
	
	private byte[] packageBytes = null;
	private int index = 0;
    private  int clientlength=0;
    private  int dataLength=0;
    
    private Date createTime;
    private Date initTime;
    private int spliceCount;
    
	public boolean isInited() {
		return isInited;
	}

	public void setInited(boolean isInited) {
		this.isInited = isInited;
	}

	public Object getProtocolMeta() {
		return protocolMeta;
	}

	public void setProtocolMeta(Object protocolMeta) {
		this.protocolMeta = protocolMeta;
	}

	public byte[] getPackageBytes() {
		return packageBytes;
	}

	public void setPackageBytes(byte[] packageBytes) {
		this.packageBytes = packageBytes;
	}

	public int getIndex() {
		return index;
	}
	
	public void setIndex(int index) {
		this.index = index;
	}
	
	public int getClientlength() {
		return clientlength;
	}
	
	public void setClientlength(int clientlength) {
		this.clientlength = clientlength;
	}
	
	public int getDataLength() {
		return dataLength;
	}
	
	public void setDataLength(int dataLength) {
		this.dataLength = dataLength;
	}

	public int getSpliceCount() {
		return spliceCount;
	}

	public void setSpliceCount(int spliceCount) {
		this.spliceCount = spliceCount;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public Date getInitTime() {
		return initTime;
	}

	public void setInitTime(Date initTime) {
		this.initTime = initTime;
	}
}
