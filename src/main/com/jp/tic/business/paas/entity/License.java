package com.jp.tic.business.paas.entity;

import java.util.Date;
import java.util.Map;

public class License {
	private String code;
	private Date startDate;
	private Date endDate;
	private long count;
	private Map<String,Long> functionCounts;
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public Date getStartDate() {
		return startDate;
	}
	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}
	public Date getEndDate() {
		return endDate;
	}
	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}
	public long getCount() {
		return count;
	}
	public void setCount(long count) {
		this.count = count;
	}
	public Map<String, Long> getFunctionCounts() {
		return functionCounts;
	}
	public void setFunctionCounts(Map<String, Long> functionCounts) {
		this.functionCounts = functionCounts;
	}
	
	
}
